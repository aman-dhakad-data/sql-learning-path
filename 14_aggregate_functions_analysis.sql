-- Task 10 — Compare COUNT(*) and COUNT(column)
-- Understand the difference between total row counts and non-NULL value counts

SELECT 
      COUNT(*) AS total_rows,
      COUNT(quantity) AS non_null_quantity
FROM sales_agg;