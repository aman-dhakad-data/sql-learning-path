-- =============================================================================
-- AGGREGATE FUNCTION PRACTICE TASKS
-- =============================================================================
-- =============================================================================
-- BASIC AGGREGATIONS
-- =============================================================================

-- Task 1 — Calculate Total Orders
-- Find the total number of orders

SELECT COUNT(*) AS total_orders
FROM sales_agg;

-- =============================================================================
-- REVENUE ANALYSIS
-- =============================================================================

-- Task 2 — Calculate Total Revenue
-- Formula: quantity * price
-- Handle NULL values using COALESCE()

SELECT 
      SUM(COALESCE(quantity,0) * COALESCE(price,0)) AS total_revenue
FROM sales_agg;

-- Task 3 — Calculate Average Order Value
-- Find the average revenue generated per order

SELECT 
      AVG(COALESCE(quantity,0) * COALESCE(price,0)) AS avg_order_value
FROM sales_agg;

-- Task 4 — Find Maximum and Minimum Prices
-- Retrieve the highest and lowest product prices

SELECT
      MAX(price) AS highest_price,
	  MIN(price) AS lowest_price
FROM sales_agg;

-- =============================================================================
-- GROUP BY ANALYSIS
-- =============================================================================
-- Task 5 — Calculate City-wise Order Counts
-- Output Columns: city, total_orders

SELECT 
      city,
      COUNT(*) AS total_orders
FROM sales_agg
GROUP BY city;

-- Task 6 — Calculate City-wise Revenue
-- Find total revenue generated by each city

SELECT 
      city,
      SUM(COALESCE(quantity,0) * COALESCE(price,0)) AS total_revenue
FROM sales_agg
GROUP BY city;

-- =============================================================================
-- DATE-BASED AGGREGATIONS
-- =============================================================================

-- Task 7 — Calculate Monthly Order Counts
-- Hint: Use DATE_TRUNC('month', order_date)

SELECT 
      DATE_TRUNC('month', order_date) AS order_month,
      COUNT(*) AS total_orders
FROM sales_agg
GROUP BY month
ORDER BY month;

-- Task 8 — Calculate Monthly Revenue

SELECT 
      DATE_TRUNC('month', order_date) AS order_month,
      SUM(COALESCE(quantity,0) * COALESCE(price,0)) AS total_revenue
FROM sales_agg
GROUP BY month
ORDER BY month;

-- =============================================================================
-- HAVING CLAUSE ANALYSIS
-- =============================================================================

-- Task 9 — Filter Cities Using HAVING
-- Retrieve cities where total revenue exceeds 1000

SELECT 
      city,
      SUM(COALESCE(quantity,0) * COALESCE(price,0)) AS total_revenue
FROM sales_agg
GROUP BY city
HAVING SUM(COALESCE(quantity,0) * COALESCE(price,0)) > 1000;

